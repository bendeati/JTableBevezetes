
package csomag;


public class Mozi {
    private String cim;
    private int ev;
    private int nezoSzam;

    public String getCim() {
        return cim;
    }

    public int getEv() {
        return ev;
    }

    public int getNezoSzam() {
        return nezoSzam;
    }

    public Mozi(String cim, int ev, int nezoSzam) {
        this.cim = cim;
        this.ev = ev;
        this.nezoSzam = nezoSzam;
    }
    
    
}
